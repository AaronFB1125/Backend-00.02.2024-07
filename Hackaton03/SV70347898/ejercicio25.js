//25. Hacer un algoritmo en JavaScript para calcular el factorial de un número de una forma distinta.

function ejecutarEjercicio25() {
    let number = parseInt(prompt("Ingrese un numero para calcular su factorial"));
    let factorial = parseInt;
    
    for (let i = 0; i < number; i++) {
        factorial = i+(number*i);
    }
    
    alert ("El factorial del número " + number + " es " + factorial);
    
       
    }
